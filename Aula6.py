# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool
print(int('1'), type(int('1'))) #Serve para converter uma substr em int
print(type(float('1') + 1))
print(bool(' ')) #bool é somente true ou false
print(str(11) + 'b')
print(str(50) + 'keven')